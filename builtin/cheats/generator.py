def render_ascii_art(text):
	text = text.upper()
	lines = ['' for _ in range(5)]

	for char in text:
		if char in ascii_art:
			art = ascii_art[char]
		else:
			art = ['_____'] * 5

		for i in range(5):
			lines[i] += art[i] + '  '


	formatted_line = '\t{\n\t' + ',\n\t'.join(f'"{line.strip()}"' for line in lines) + '\n\t},'
	return formatted_line
	
